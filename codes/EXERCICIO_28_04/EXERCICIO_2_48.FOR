	INTEGER FDA, MATRIC, UE, US
	CHARACTER*20 OUTDA
	US = 4
	UE = 5
	OPEN(UE,FILE='ENTRADA.TXT',STATUS='OLD')
	OPEN(US,FILE='CON')
10	CONTINUE
		READ(UE,11,IOSTAT=FDA) MATRIC,OUTDA
11		FORMAT(I6,A20)
		IF(FDA .NE. 0 .OR. MATRIC .EQ.830008) GO TO 20
		GO TO 10
20	CONTINUE
	IF(FDA .NE. 0)THEN
		WRITE(US,22)
22		FORMAT(1X,'REGISTRO NAO ENCONTRADO')
	ELSE
		WRITE(US,24)
24		FORMAT(1X,'REGISTRO ENCONTRADO')
		WRITE(US,26)MATRIC,OUTDA
26		FORMAT(1X,I6,3X,A20)
	END IF
	CLOSE(UE,STATUS='KEEP')
	STOP
	END