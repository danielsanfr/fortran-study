	CHARACTER*1 GABARI(10),RESPOS(10)
	INTEGER FREQUE
	DIMENSION FREQUE(0:10)
	INTEGER APROVA,MAIOR,NOTA,NOTAMA,NUMERO,TOTAL
	INTEGER UE,US
	REAL PORCEN
	UE = 5
	US = 6
	TOTAL = 0
	DO 10 L = 0,10,1
		FREQUE(L) = 0
10	CONTINUE
	READ(UE,11) (GABARI(L),L=1,10,1)
11	FORMAT(10A1)
	WRITE(US,12)
12	FORMAT(1H ,'NUMERO',2X,'NOTA')
20	CONTINUE
		READ(UE,21) NUMERO,(RESPOS(L),L=1,10,1)
21		FORMAT(I4,1X,10A1)
		IF (NUMERO.EQ.9999) GO TO 40
		TOTAL = TOTAL + 1
		NOTA = 0
		DO 30 L = 1,10,1
			IF (GABARI(L).EQ.RESPOS(L)) THEN
				NOTA = NOTA + 1
			END IF
30		CONTINUE
		WRITE(US,32) NUMERO,NOTA
32		FORMAT(1H ,1X,I4,5X,I2)
		FREQUE(NOTA) = FREQUE(NOTA) + 1
	GO TO 20
40	CONTINUE
	APROVA = 0
	DO 50 L = 6,10,1
		APROVA = APROVA + FREQUE(L)
50	CONTINUE
	PORCEN = FLOAT(APROVA) / FLOAT(TOTAL) * 100.0
	MAIOR = 0
	DO 60 L = 0,10,1
		IF (FREQUE(L).GT.MAIOR) THEN
			MAIOR = FREQUE(L)
			NOTAMA = L
		END IF
60	CONTINUE
	WRITE(US,62) NOTAMA,PORCEN
62	FORMAT(1H ,'NOTA DE MAIOR FREQUENCIA: ', I2,/,
	1	   1H ,'PORCENTAGEM DE APROVADOS: ',F5.1)
	STOP
	END