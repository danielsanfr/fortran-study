C		DEFINICAO DE VARIAEIS
C		MATRIC	-	NUMERO DE MATRICULA
C		RESTO	-	DEMAIS INFORMACOES DO REGISTRO
C		CHAVE	-	POSICAO DO REGISTRO NO ARQUIVO
C		UE		-	UNIDADE DE ENTRADA
C		US		-	UNIDADE DE SAIDA
	INTEGER MATRIC, V, CHAVE, UE, US
	CHARACTER*20 RESTO
C		DEFINICAO DAS UNIDADES DE ENTRADA/SAIDA
	US = 0
	UE = 2
C		ABERTURA DOS ARQUIVOS
	OPEN(UE,FILE='DADOS.TXT',STATUS='OLD',ACCESS='DIRECT',
     *	FORM='FORMATTED',RECL=26)
C		LEITURA DO REGISTRO USANDO METRICULA COMO CHAVE
	CHAVE = 5
	READ(2,11,REC=CHAVE,ERR=20) MATRIC, RESTO
11	FORMAT(I6,A20)
	WRITE(US,12) RESTO
12	FORMAT(1X,A20)
	GO TO 30
20	WRITE(US,22) CHAVE
22	FORMAT(' CHAVE',I2,' NAUM EXISTE NO ARQUIVO')
30	STOP
	END