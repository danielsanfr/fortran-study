C		DECLARACAO DE  VARIAVEIS
	INTEGER PRODUC(12,2),CLM(12,2),VALORE(2,2),CLA(2)
C		PRODUC - PRODUCAO MENSAL DE CADA MOTOR
C		CLM - CUSTO E LUCRO MENSAIS DOS MOTORES
C		VALORE - CUSTO E LUCRO DE CADA MOTOR
C		CLA - CUSTO E LUCRO ANUAIS DOS MOTORES
	INTEGER I,J,K
	INTEGER UE,US
	UE = 5
	US = 6
	READ(UE,1) ((PRODUC(I,J),J=1,2,1),I=1,12,1),
     *		   ((VALORE(I,J),J=1,2,1),I=1,2,1)
1	FORMAT(I2,2X,I2)
C		CALCULO DO CUSTO E DO LUCRO MENSAIS
	DO 10 I = 1,12,1
		DO 10 J = 1,2,1
			CLM(I,J) = 0
			DO 10 K = 1,2,1
				CLM(I,J) = CLM(I,J) + PRODUC(I,K)*VALORE(K,J)
10	CONTINUE
C		CALLCULO DO CUSTO E DO LUCRO ANUAIS
	DO 20 J = 1,2,1
		CLA(J) = 0
		DO 20 I = 1,12,1
			CLA(J) = CLA(J) + CLM(I,J)
20	CONTINUE
C		ESCRITA DOS RESULTADOS OBTIDOS
	WRITE(US,22)
22	FORMAT(1H1,	'MES CUSTO LUCRO')
	WRITE(US,24) (I,(CLM(I,J),J=1,2,1),I=1,12,1)
24	FORMAT(1H ,2X,I2,3X,I3,2X,I3)
	WRITE(US,26)
26	FORMAT(1H0,'CUSTOS E LUCROS ANUAIS')
	WRITE(US,28) CLA(1),CLA(2)
28	FORMAT(1H ,1X,I4,6X,I3)
	STOP
	END