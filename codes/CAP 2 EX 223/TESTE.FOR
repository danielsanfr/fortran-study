C	DECLARACAO DE VARIAVEIS
	INTEGER A(4,4)
	INTEGER J,K
	INTEGER UE,US
	UE = 5
	US = 6
	READ(UE,1) ((A(I,K),K=1,4,1),I=1,4,1)
1	FORMAT(4(I2,3X))
	READ(UE,3) K
3	FORMAT(I2)
C	CALCULO DO PRODUTO DA DIAGONAL PRINCIPAL POR K
	DO 10 I = 1,4,1
		A(I,I) = A(I,I)*K
10	CONTINUE
	WRITE(US,12)((A(I,K),K=1,4,1),I=1,4,1)
12	FORMAT(1H ,4(4(I2,2X),/,1H ))
	STOP
	END