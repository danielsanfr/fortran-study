C		DEFINICAO DE VARIAEIS
C		PLACA	-	NUMERO DA PLACA
C		TIPO	-	TIPO DE VEICULO
C		PROPTA	-	PROPRIETARIO
C		UE		-	UNIDADE DE ENTRADA
C		US		-	UNIDADE DE SAIDA
	INTEGER PLACA, UE, US
	CHARACTER TIPO*10, PROPTA*30
C		DEFINICAO DAS UNIDADES DE ENTRADA/SAIDA
	UE = 2
	US = 3
C		ABERTURA DOS ARQUIVOS
	OPEN(UNIT=UE,FILE='FONTE.TXT',STATUS='OLD')
	OPEN(UNIT=US,FILE='VEICULOS.TXT',STATUS='NEW',ACCESS='DIRECT',
     *	 FORM='FORMATTED',RECL=40)
C		CRIACAO DO ARQUIVO DE ACESSO DIRETO
10	CONTINUE
		READ(UE,11,END=20) PLACA, TIPO, PROPTA
11		FORMAT(I4,A10,A30)
		WRITE(US,12,REC=PLACA) TIPO,PROPTA
12		FORMAT(A10,A30)
	GO TO 10
20	CONTINUE
C		FECHAMENTO DOS ARUIVOS
	CLOSE(UE,STATUS='KEEP')
	CLOSE(US,STATUS='KEEP')
	STOP
	END