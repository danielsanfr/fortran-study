C	DEFINICAO DAS VARIAVEIS
	INTEGER MAT(4,5)
	INTEGER	I,J,SOMALI,TOTAL
	INTEGER UE,US
	UE = 5
	US = 6
	TOAL = 0
	READ(UE,1) ((MAT(I,J),J=1,5,1),I=1,4,1)
1	FORMAT(4(5(I2,2X)/))
	DO 20 I = 1,4,1
C	CALCULO DA SOMA DOS ELEMENTOS DA LINHA
		SOMALI = 0
		DO 10 J = 1,5,1
			SOMALI = SOMALI+MAT(I,J)
10		CONTINUE
		WRITE(US,12) I,SOMALI
12		FORMAT(1H ,'SOMA DA LINHA ',I2,':',I4)
C		CALCULO DA SOMA DE TODOS ELEMENTOS
		TOTAL = TOTAL + SOMALI
20	CONTINUE
C	ESCRITA DA SOMA DE TODOS ELEMENTOS
	WRITE(US,22) TOTAL
22	FORMAT(1H0,'TOTAL:',I4)
	STOP
	END