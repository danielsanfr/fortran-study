	INTEGER CODIGO(100)
	CHARACTER NOME(100) * 15
	INTEGER CODESE, K ,J ,I ,UE ,US

	UE = 5
	US = 6

	READ(5,1) (CODIGO(I),NOME(I),I=1,2)
1	FORMAT(I5,A15)
	DO 30 K = 1,500
	READ(UE,3)CODESE
3	FORMAT(I5)
	I = 0
10	CONTINUE
	I = I + 1
	IF(CODIGO(I).EQ.CODESE.OR.I.EQ.2)GOTO 20
	GOTO 10
20	CONTINUE
	IF(CODIGO(I).EQ.CODESE)THEN
C	ESCRITA DO NOME DA PROFISSÃO
	WRITE(US,22)CODESE,NOME(I)
22	FORMAT(1H0,I5,1X,A15)
	ELSE
	WRITE(US,24)CODESE
24	FORMAT(1H0,I5,1X,'CODIGO INVALIDO')
	END IF
30	CONTINUE
	STOP
	END