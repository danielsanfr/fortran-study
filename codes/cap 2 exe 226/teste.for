C		DECLARACAO DA VARIAVEIS
	CHARACTER APOSTA(3,16)*1,NREV*12,NAPOST*16
	INTEGER D,I,N,NMAQ,NTESTE,T,J
	REAL V,VALOR
	INTEGER US,UE
	US = 6
	UE = 5
C		LEITURA DOS DADOS DO TESTE E REVENDEDOR
	READ(UE,1) NTESTE,NMAQ,NREV,NAPOST,V
1	FORMAT(I4,2X,I5,/,A12,3X,A6,3X,F4.0)
	READ(UE,3) ((APOSTA(I,J),I=1,3,1),J=1,16,1)
3	FORMAT(3A1)
C		VERIFICACAO DO NUMERO DE DUPLOS E TRIPLOS
	D = 0
	T = 0
	DO 10 J = 1,16,1
		N = 0
		IF (APOSTA(1,J).EQ.'X') N = N + 1
		IF (APOSTA(2,J).EQ.'X') N = N + 1
		IF (APOSTA(3,J).EQ.'X') N = N + 1
		IF (N.EQ.3) THEN
			T = T + 1
		ELSE
			IF (N.EQ.2) THEN
				D = D + 1
			END IF
		END IF
10	CONTINUE
	VALOR = V*2**D*3**T
C		ESCRITA DOS RESULTADOS OBTIDOS
	WRITE(US,12) NTESTE,NMAQ,NREV,NAPOST
12	FORMAT(1H1,7X,'LOTERIA ESPORTIVA FEDERAL',/,
     1	   1H0,7X,'TESTE N. ',I4,5X,'MAQ ', I5,/,
     2	   1H ,' REV 'A12,1X,'APOSTA N. ',A16)
	WRITE(US,14) (I,I=1,16)
14	FORMAT(1H0,' ORDEM/',9(I2),7(I3),/1H ,46('='))
	WRITE(US,16) ((APOSTA(4-I,J),J=1,16,1),I=1,3,1)
16	FORMAT(1H ,' COL 2/',10(1X,A1),6(2X,A1),/
     *	   1H ,'EMPATE/',10(1X,A1),6(2X,A1),/
     *	   1H ,' COL 1/',10(1X,A1),6(2X,A1))
	WRITE(US,18) VALOR
18	FORMAT(1H ,46('='),/
     *	   1H ,12X,'VALOR PAGO',13X,'R',F8.2)
	STOP
	END